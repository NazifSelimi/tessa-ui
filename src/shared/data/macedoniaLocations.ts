function buildMunicipalityLabel(location: MacedoniaLocation): string {
  if (!location.settlement || location.settlement === location.city) {
    return location.city;
  }

  return `${location.city} (${location.settlement})`;
}

export const MACEDONIA_CITY_OPTIONS = MACEDONIA_LOCATIONS.map((location) => ({
  value: location.city,
  label: buildMunicipalityLabel(location),
}));

export const MACEDONIA_POSTCODE_OPTIONS = MACEDONIA_LOCATIONS.map((location) => ({
  value: location.postcode,
  label: `${location.postcode} - ${buildMunicipalityLabel(location)}`,
}));

export function getPostcodeForCity(city: string | null | undefined): string {
  return MACEDONIA_LOCATIONS.find((location) => location.city === city)?.postcode ?? '';
}

export function getCityForPostcode(postcode: string | null | undefined): string {
  return MACEDONIA_LOCATIONS.find((location) => location.postcode === postcode)?.city ?? '';
}
